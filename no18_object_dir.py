# dirでメソッドや付加情報を確認

# string
print("string:")
print(dir("text"), "\r\n\r\n")

# int
print("int:")
print(dir(1), "\r\n\r\n")

# boolean
print("boolean:")
print(dir(True), "\r\n\r\n")

# float
print("float:")
print(dir(3.14), "\r\n\r\n")

# array
print("array:")
print(dir(["Banana", "Apple", "Fish", "Meat", "Milk"]), "\r\n\r\n")
