# 複数の値を複数の変数に割り当てられる

list_a, list_b, list_c = [1, 2, 3]
print("list:", list_a, list_b, list_c) # list: 1 2 3

tuple_a, tuple_b, tuple_c = (1, 2, 3)
print("tuple:", tuple_a, tuple_b, tuple_c) # tuple: 1 2 3

set_a, set_b, set_c = {1, 2, 3}
print("set:", set_a, set_b, set_c) # set: 1 2 3

dict_a, dict_b, dict_c = {"鈴木": 44, "坂東": 25, "小林": 29}
print("dict:", dict_a, dict_b, dict_c) # dict: 鈴木 坂東 小林
