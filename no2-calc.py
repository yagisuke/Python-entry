# 和
print(3 + 5) # 8
# 差
print(5 - 2) # 3
# 積
print(2 * 3) # 6
# 商
print(10 / 5) # 2.0
# 商の小数点以下の切り捨て
print(10 // 3) # 3
# 商の余り
print(10 % 3) # 1
# 符号の反転
print(-5) # -5
# 乗
print(2 ** 3) # 8
# 絶対値
print(abs(-8)) # 8
# 整数
print(int(3.14)) # 3
# 浮動小数点に変換
print(float(3)) # 3.0
# 桁に丸める
print(round(3.6)) # 4
